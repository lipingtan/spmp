    `bill_no` VARCHAR(20) DEFAULT NULL COMMENT '账单编号（冗余）',
    `refund_amount` DECIMAL(12,2) NOT NULL COMMENT '退款金额（负数表示）',
    `refund_reason` VARCHAR(512) NOT NULL COMMENT '退款原因',
    `operator_id` BIGINT NOT NULL COMMENT '操作人ID',
    `operator_name` VARCHAR(64) DEFAULT NULL COMMENT '操作人姓名（冗余）',
