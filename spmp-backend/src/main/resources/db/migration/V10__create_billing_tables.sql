    KEY `idx_bill_id` (`bill_id`),
    KEY `idx_urge_user_id` (`urge_user_id`),
    KEY `idx_urge_time` (`urge_time`),
    KEY `idx_urge_type` (`urge_type`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci COMMENT='催收记录表';

CREATE TABLE `bl_refund_record` (
    `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键',
